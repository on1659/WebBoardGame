// Helper to create empty 15x15 board
function empty(size = 15) {
  return Array.from({ length: size }, () => Array(size).fill(0));
}

function setStones(size, stones) {
  const b = empty(size);
  for (const [r, c, color] of stones) b[r][c] = color;
  return b;
}

