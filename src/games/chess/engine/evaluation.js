/**
 * Evaluate the board position
 * @param {Chess} chess - chess.js instance
 * @returns {number} - Score from black's perspective (positive = good for black/AI)
 */
export function evaluateBoard(chess) {
  const board = chess.board();
  let score = 0;

  for (let row = 0; row < 8; row++) {
    for (let col = 0; col < 8; col++) {
      const piece = board[row][col];
      if (!piece) continue;

      const pieceValue = PIECE_VALUES[piece.type];

      // Calculate PST index
      // For white: index = row * 8 + col (a8=0)
      // For black: flip vertically
      const pstIndex = piece.color === 'w'
        ? row * 8 + col
        : (7 - row) * 8 + col;

      const positionBonus = PST[piece.type][pstIndex];
      const totalValue = pieceValue + positionBonus;

      // Positive for black (AI), negative for white (player)
      score += piece.color === 'b' ? totalValue : -totalValue;
    }
  }

  return score;
}
